//  Operators and Different Types of Operators:
// In programming, an operator is a symbol or keyword that performs a specific operation on one or more operands (values or variables). There are different types of operators, such as:

// Arithmetic Operators: These operators are used to perform mathematical operations, such as addition, subtraction, multiplication, division, and modulus.

// Comparison Operators: These operators are used to compare two values and return a boolean value (true or false) based on the comparison. Examples of comparison operators include greater than (>), less than (<), equal to (==), and not equal to (!=).

// Logical Operators: These operators are used to combine two or more boolean expressions and return a boolean result. Examples of logical operators include AND (&&), OR (||), and NOT (!).

// Assignment Operators: These operators are used to assign a value to a variable. Examples of assignment operators include equals (=), plus-equals (+=), and minus-equals (-=).

// Unary Operators: These operators work on a single operand and include increment (++), decrement (--), and negation (-).

// Ternary Operator: This operator is a shorthand way of writing an if-else statement in a single line.

